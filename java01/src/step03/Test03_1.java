/* 기타 연산자:관계 연산자 - ==, !=, <, <=, >, >=
 * */
package step03;

public class Test03_1 {
  public static void m1(int value) {
    System.out.println(value);
  }

  public static void main(String[] args) {
     System.out.println(5 == 3);
     System.out.println(5 != 3);
     System.out.println(5 < 3);
     System.out.println(5 <= 3);
     System.out.println(5 > 3);
     System.out.println(5 >= 3);
  }
 }

